// Paged GQA Decode (Q_len=1) for Qwen3-30B-A3B shapes
// B200 (sm_100a), BF16, GQA8 (8 QO heads sharing 1 KV head)
// Architecture: Split-KV + SMEM-staged KV + warp-per-head
// Each CTA: 256 threads = 8 warps, one warp per QO head
// KV loaded once to SMEM, shared across all 8 heads

#include <cuda.h>
#include <cuda_runtime.h>
#include <cuda_bf16.h>
#include <stdio.h>
#include <stdlib.h>
#include <math.h>
#include <algorithm>
#include <vector>
#include <random>

using bf16_t = __nv_bfloat16;

static constexpr int HD = 128;
static constexpr int NQH = 8;
static constexpr int NKH = 1;
static constexpr int PS = 4096;
static constexpr int64_t KVS_TOK = (int64_t)NKH * HD;
static constexpr int64_t KVS_KV  = (int64_t)PS * KVS_TOK;
static constexpr int64_t KVS_PAGE = 2 * KVS_KV;

static constexpr int BLK = 256;
static constexpr int CHUNK = 32; // tokens per chunk
// SMEM: K_buf[CHUNK][HD] + V_buf[CHUNK][HD] + scores[NQH][CHUNK]
// = 2*32*256 + 8*32*4 = 16384 + 1024 = 17408 bytes

struct SmemLayout {
    bf16_t kbuf[CHUNK][HD];        // 8192 bytes for K
    bf16_t vbuf[CHUNK][HD];        // 8192 bytes for V
    float  sc[NQH][CHUNK];         // 1024 bytes for scores
};

__global__ void __launch_bounds__(BLK)
paged_gqa_decode_v3(
    const bf16_t* __restrict__ Q,
    const bf16_t* __restrict__ paged_kv,
    const int* __restrict__ kv_indices,
    float* __restrict__ pO,       // [nsplits, NQH, HD]
    float* __restrict__ pLSE,     // [nsplits, NQH]
    int seq_len, int chunk_size, float sm_scale_log2
) {
    __shared__ SmemLayout sm;
    
    const int sid = blockIdx.x;  // split index
    const int tid = threadIdx.x;
    const int wid = tid / 32;    // warp 0-7 = head 0-7
    const int lid = tid % 32;
    
    const int ts = sid * chunk_size;
    const int te = min(ts + chunk_size, seq_len);
    
    if (ts >= seq_len) {
        if (lid == 0) pLSE[sid * NQH + wid] = -INFINITY;
        for (int d = lid; d < HD; d += 32)
            pO[(sid * NQH + wid) * HD + d] = 0.0f;
        return;
    }
    
    // Load Q into registers: 4 elements per thread
    float qr[4];
    #pragma unroll
    for (int i = 0; i < 4; i++)
        qr[i] = __bfloat162float(Q[wid * HD + lid * 4 + i]);
    
    float m_val = -INFINITY, l_val = 0.0f;
    float oa[4] = {0, 0, 0, 0};
    
    for (int cs = ts; cs < te; cs += CHUNK) {
        int ce = min(cs + CHUNK, te);
        int cl = ce - cs;
        
        // === Step 1: Load K and V to SMEM using uint4 (128-bit) loads ===
        // Precompute base pointer: since CHUNK=32 << PS=4096, at most 1 page crossing
        {
            const int pl0 = cs / PS;
            const int tip0 = cs % PS;
            const int pp0 = __ldg(kv_indices + pl0);
            const bf16_t* kbase0 = paged_kv + (int64_t)pp0 * KVS_PAGE + (int64_t)tip0 * KVS_TOK;
            const bf16_t* vbase0 = kbase0 + KVS_KV;
            
            // Check if chunk crosses page boundary
            const int page_remain = PS - tip0; // tokens remaining in this page
            const bool crosses = (cl > page_remain);
            
            const int total_u4 = cl * (HD / 8);
            for (int idx = tid; idx < total_u4; idx += BLK) {
                int tok_local = idx / (HD / 8);
                int u4_idx = idx % (HD / 8);
                
                const bf16_t* kp;
                const bf16_t* vp;
                if (!crosses || tok_local < page_remain) {
                    kp = kbase0 + (int64_t)tok_local * KVS_TOK;
                    vp = vbase0 + (int64_t)tok_local * KVS_TOK;
                } else {
                    // Second page
                    int pp1 = __ldg(kv_indices + pl0 + 1);
                    int tip1 = tok_local - page_remain;
                    kp = paged_kv + (int64_t)pp1 * KVS_PAGE + (int64_t)tip1 * KVS_TOK;
                    vp = kp + KVS_KV;
                }
                ((uint4*)sm.kbuf[tok_local])[u4_idx] = __ldg(((const uint4*)kp) + u4_idx);
                ((uint4*)sm.vbuf[tok_local])[u4_idx] = __ldg(((const uint4*)vp) + u4_idx);
            }
        }
        __syncthreads();
        
        // === Step 2: QK^T + online softmax + PV (fused, no extra syncthreads) ===
        {
            // First pass: compute all scores and find chunk max
            float scores_reg[CHUNK]; // scores in registers per warp
            float chunk_max = -INFINITY;
            #pragma unroll 4
            for (int t = 0; t < cl; t++) {
                float dot = 0.0f;
                #pragma unroll
                for (int i = 0; i < 4; i++)
                    dot += qr[i] * __bfloat162float(sm.kbuf[t][lid * 4 + i]);
                #pragma unroll
                for (int o = 16; o > 0; o >>= 1)
                    dot += __shfl_xor_sync(0xFFFFFFFF, dot, o);
                float score = dot * sm_scale_log2;
                scores_reg[t] = score;
                chunk_max = fmaxf(chunk_max, score);
            }
            
            // Online softmax update
            float nm = fmaxf(m_val, chunk_max);
            float corr = exp2f(m_val - nm);
            
            float chunk_sum = 0.0f;
            for (int t = 0; t < cl; t++) {
                float p = exp2f(scores_reg[t] - nm);
                scores_reg[t] = p;
                chunk_sum += p;
            }
            
            #pragma unroll
            for (int i = 0; i < 4; i++)
                oa[i] *= corr;
            l_val = l_val * corr + chunk_sum;
            m_val = nm;
            
            // PV accumulation (no need for extra syncthreads - V is already loaded)
            for (int t = 0; t < cl; t++) {
                float p = scores_reg[t];
                #pragma unroll
                for (int i = 0; i < 4; i++)
                    oa[i] += p * __bfloat162float(sm.vbuf[t][lid * 4 + i]);
            }
        }
        __syncthreads();
    }
    
    // Normalize and write
    float inv_l = (l_val > 0.0f) ? __fdividef(1.0f, l_val) : 0.0f;
    float lse = (l_val > 0.0f) ? (m_val + __log2f(l_val)) : -INFINITY;
    
    if (lid == 0) pLSE[sid * NQH + wid] = lse;
    #pragma unroll
    for (int i = 0; i < 4; i++)
        pO[(sid * NQH + wid) * HD + lid * 4 + i] = oa[i] * inv_l;
}

// ========================= Combine Kernel =========================
__global__ void combine_kernel(
    const float* __restrict__ pO, const float* __restrict__ pLSE,
    bf16_t* __restrict__ Out, int nsplits
) {
    int h = blockIdx.x, d = threadIdx.x;
    if (h >= NQH || d >= HD) return;
    
    float mx = -INFINITY;
    for (int s = 0; s < nsplits; s++)
        mx = fmaxf(mx, pLSE[s * NQH + h]);
    
    float so = 0, sw = 0;
    for (int s = 0; s < nsplits; s++) {
        float w = exp2f(pLSE[s * NQH + h] - mx);
        sw += w;
        so += w * pO[(s * NQH + h) * HD + d];
    }
    Out[h * HD + d] = __float2bfloat16(sw > 0 ? __fdividef(so, sw) : 0.0f);
}

// ========================= Host =========================
void chk(cudaError_t e, const char* m) { if (e != cudaSuccess) { fprintf(stderr, "CUDA(%s):%s\n", m, cudaGetErrorString(e)); exit(1); } }

void cpu_ref(const bf16_t* Q, const bf16_t* kv, const int* ki, int sl, float sms, float* out) {
    for (int h = 0; h < NQH; h++) {
        float q[HD]; for (int d = 0; d < HD; d++) q[d] = __bfloat162float(Q[h*HD+d]);
        float m=-INFINITY, l=0; float o[HD]={}; 
        for (int t = 0; t < sl; t++) {
            int pl=t/PS, tip=t%PS, pp=ki[pl];
            const bf16_t* k=kv+(int64_t)pp*KVS_PAGE+(int64_t)tip*KVS_TOK;
            float dot=0; for(int d=0;d<HD;d++) dot+=q[d]*__bfloat162float(k[d]);
            float sc=dot*sms, nm=fmaxf(m,sc), c=expf(m-nm), p=expf(sc-nm);
            l=l*c+p;
            const bf16_t* v=kv+(int64_t)pp*KVS_PAGE+KVS_KV+(int64_t)tip*KVS_TOK;
            for(int d=0;d<HD;d++) o[d]=o[d]*c+p*__bfloat162float(v[d]);
            m=nm;
        }
        float il=l>0?1.0f/l:0; for(int d=0;d<HD;d++) out[h*HD+d]=o[d]*il;
    }
}

